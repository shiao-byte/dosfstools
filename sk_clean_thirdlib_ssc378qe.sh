#!/bin/bash

#############################################################################################################
### Set global Env
#############################################################################################################
pwd=$PWD
CHIP=ssc378qe
OUT_NAME=thridlib_out

echo "cleaning thirdlib for ${CHIP}..."

#############################################################################################################
### Clean sub modules
#############################################################################################################

#####################
### clean libiconv
#####################
cd $pwd/libiconv
./sk_clean.sh

#####################
### clean dosfstools
#####################
cd $pwd/dosfstools
./sk_clean.sh

#############################################################################################################
### Clean output
#############################################################################################################
cd $pwd

#####################
### Remove output directory
#####################
echo "rm output path: ${OUT_NAME}"
rm -rf ${OUT_NAME}

#####################
### Remove release package
#####################
rm -f ${OUT_NAME}.tar.gz

echo "clean thirdlib ok!!!"
